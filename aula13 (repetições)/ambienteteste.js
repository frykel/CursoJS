var c = 1
var b = 1
while (c<=10 ){
    console.log(`Passo ${c}`)
    c++ // c = c + 1
}
do {
    console.log (`Step ${b}`)
    b++
} while(b<=10)

for (var d = 1; d <= 10; d++){

    console.log (`Passo ${d}`)
}
/* Tipos de Repetições:
    While --> Faz o bloco e repete o bloco enquanto a condição for verdade
    Do While --> Executa o bloco e depois vê se a condição é verdadeira para poder repetir
    for --> Estrutura de repetição com variável de controle for (inicialização ; teste lógico ; incremento)
*/





